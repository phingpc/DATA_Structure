    // Save 1: Plain text
    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed

        if (data != null) {
            try {
                FileWriter fileWriter = new FileWriter("data.txt");
                PrintWriter printWriter = new PrintWriter(fileWriter);
                for (Traveler x : t) {
                    printWriter.println(x.toString());
                }
                printWriter.close();
                fileWriter.close();

                JOptionPane.showMessageDialog(this, "Plain text data has been saved successfully", "Save 1", JOptionPane.INFORMATION_MESSAGE);

            } catch (Exception e) {
            };
        } else {
            JOptionPane.showMessageDialog(this, "Please generate the data first", "No data", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_jButton3ActionPerformed

