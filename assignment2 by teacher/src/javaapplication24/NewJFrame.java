class Traveler implements Serializable {

    private String passportId;
    private String name;
    private String gender;
    private int age;
    private String origin;
    private String destination;

    // Overload constructor
    public Traveler(String p, String n, String g, int a, String o, String d){
        passportId = p;
        name = n;
        gender = g;
        age = a;
        origin = o;
        destination = d;
    }
    
    // Overriden Default constructor
    public Traveler() {
        String str1 = "ABCDEFGHIJKLMNOPQRSTUVWXYZ";
        String str2 = "0123456789";

        Random r = new Random();
        String temp = "";

        // Generate a random passportID string
        for (int i = 0; i < 2; i++) {
            temp += str1.charAt(r.nextInt(str1.length()));
        }
        for (int i = 0; i < 4; i++) {
            temp += str2.charAt(r.nextInt(str2.length()));
        }
        this.passportId = temp;

        // Generate a random name
        temp = "";
        String str3 = "abcdefghijklmnopqrstuvwxyz";
        // firstname
        int n = r.nextInt(9) + 2;
        for (int i = 0; i < n; i++) {
            if (i == 0) // capital letter
            {
                temp += str1.charAt(r.nextInt(str1.length()));
            } else {
                temp += str3.charAt(r.nextInt(str3.length()));
            }
        }
        temp += " ";
        // lastname
        n = r.nextInt(9) + 2;
        for (int i = 0; i < n; i++) {
            if (i == 0) {
                temp += str1.charAt(r.nextInt(str1.length()));
            } else {
                temp += str3.charAt(r.nextInt(str3.length()));
            }
        }
        this.name = temp;

        // Generate a random gender
        String[] str4 = {"Male", "Female"};
        this.gender = str4[r.nextInt(str4.length)];

        // Generate a random int for the age
        this.age = r.nextInt(100) + 1;

        // Generate a random origin
        String[] str5 = {"Japan", "China", "USA", "Australia", "Canada", "Russia", "India"};
        this.origin = str5[r.nextInt(str5.length)];

        // Generate a random destination
        String[] str6 = {"Bangkok", "Pattaya", "Koh-samui", "Phuket", "Burirum"};
        this.destination = str6[r.nextInt(str6.length)];
    }

    public String toString() {
        return passportId + "," + name + "," + gender + "," + age + "," + origin + "," + destination;
    }

}

